<p>Research Guidelines Manuscript Preparation Guidelines</p>

<p><b>1. Language :</b><br/>
The language of the manuscript must be in English (either American or British standard, but not the mixture of both).:<br/>

<b>2. Length of paper:</b><br/>
The length of the paper should not exceed 25 pages (Times New Roman, 12 Font) excluding tables, figures, references and appendices (if any). Paper containing more than 25 pages words will be returned to the author(s) to abridge. Articles should be typed in double-space (including footnotes and references) on one side of the paper only (preferably A4) with wide margins. Authors are urged to write as concisely as possible, but not at the expense of clarity.:<br/>

<b>3. Title Page:</b><br/>
The title page should include::<br/>
The name(s) of the author(s):<br/>
A concise and informative title:<br/>
The affiliation(s) and address(es) of the author(s):<br/>
The e-mail address, telephone and fax numbers of the corresponding author
Name of the Journal:<br/>
Since ARIPD follows central submission policy for all journals (same e-mail for submission in any journal), the corresponding author is requested to mention the name of the journal in which he/she is interested to publish his/her paper.:<br/>

Abstract:<br/>
Please provide an abstract of 150 to 200 words. The abstract should not contain any undefined abbreviations or unspecified references.:<br/>

Keywords:<br/>
Please provide 4 to 6 keywords which can be used for indexing purposes.:<br/>

Acknowledgement:<br/>
The author may use acknowledgement section in the title page of the paper (if any).:<br/>

<b>4. Subdivision of the article:</b><br/>

Divide your article into clearly defined and numbered sections. Subsections should be numbered 1, 2. (then 1.1, 1.1.1, 1.1.2), 1.2, etc. The abstract is not included in section numbering.:<br/>

<b>5. Table and Figures:</b><br/>
Present tables and figures within the article, not at the end of the article. Please note that the article will be published in black and white (print), although online version will contain the colorful figures (if any). However, the color print will be available in extreme cases as per the request of the author. In that case, the author has to pay additional 20 USD with the original publication fee.:<br/>

<b>6. References:</b><br/>
Author(s) should follow the latest edition of APA style in referencing. Please visit www.apastyle.org to learn more about APA style.:<br/>

Citations in the text:<br/>
Please ensure that every reference cited in the text is also present in the reference list (and vice versa). Avoid citation in the abstract. Unpublished results and personal communications should not be in the reference list, but may be mentioned in the text. Citation of a reference as 'in press' implies that the item has been accepted for publication.:<br/>

Reference List:<br/>
References should be arranged first alphabetically and then further sorted chronologically if necessary. More than one reference from the same author(s) in the same year must be identified by the letters "a", "b", "c", etc., placed after the year of publication.:<br/>

Examples::<br/>
Reference to a journal publication::<br/>
Van der Geer, J., Hanraads, J. A. J., & Lupton R. A. (2000). The art of writing a scientific article. Journal of Scientific Communications, 163, 51-59.:<br/>

<b>7. Submission Preparation Checklist:</b><br/>
Before submitting the manuscript, author(s) should check the following list.:<br/>
•	The submission has not been previously published, nor is it before another journal for consideration.:<br/>
•	The submission file is in Microsoft Word file.:<br/>
•	Manuscript has been made in the light of Journal’s author guidelines.:<br/>
•	Author(s) did not mention his or her name and affiliation in the main text:<br/>
•	Author (s) has mentioned the name of the journal (in which the author is interested to publish) in title page.:<br/>
•	Author(s) has read all the terms and conditions of the journal.:<br/>
•	Author is aware of the publication fee of the journal:<br/>


