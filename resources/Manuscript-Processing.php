<p><b>Plagiarism</b></p><br/>
<p>Plagiarism is copying another person’s text or ideas and passing the copied material as your own work. You must both delineate (i.e., separate and identify) the copied text from your text and give credit to (i.e., cite the source) the source of the copied text to avoid accusations of plagiarism. Plagiarism is considered fraud and has potentially harsh consequences including loss of job, loss of reputation, and the assignation of reduced or failing grade in a course”.
All manuscripts received towards Issra journals are scanned for plagiarism. If potential plagiarism is detected, authors will be contacted for clarification. The authors should ensure that they have written entirely original works, and if the authors have used the work and/or words of others, it is necessary to indicate the source of the information within your paper using an internal citation.
</p>

