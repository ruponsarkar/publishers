<p><b>Peer Review Policy</b></p>

<p>The practice of peer review policy is to ensure that only good science is published. It is an objective process at the heart of good scholarly publishing and is carried out by all reputable scientific journals. Our referees play a vital role in maintaining the high standards Transport Policy and all manuscripts are peer reviewed following the procedure outlined below</p>

<p><b>☛ Processing fee</b></p>

<p>Issra Publishers is an online international open access, peer reviewed journals publishing organization; we do not have funding/support from any Private Organizations/Government to run. To run our organization in an organized way, we charge manuscript processing fee from the authors to publish their manuscripts. We charge fee to maintain published manuscripts in online repositories. All our articles are published in most updated formats (HTML, PDF, etc.,)</p>

<p><b>☛ Manuscript Withdrawal Policy</b></p>

<p>Author may wish to withdraw his/her manuscript after submitting it to Issra Publishers. To withdraw a manuscript, we do not charge any fee, only if he/she withdraws it within a week.
If an author withdraws manuscript after a week, then he/she will be asked to pay partial processing fee. Each manuscript submission costs us (ISSRA Publishers) certain amount along with overhead cost during processing. Manuscripts peer review process is completed (which can often happen within a week), and it has been worked into a journal issue, we have incurred costs on our end that need to be reimbursed.
We ask authors to pay 30% of the original article processing fee in case of withdrawal
For any queries or any discussions regarding manuscript withdrawal/article processing fee, please contact us at: issrapublishers@gmail.com 
</p>


















