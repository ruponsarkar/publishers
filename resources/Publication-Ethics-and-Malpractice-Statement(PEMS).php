<p style="font-weight: bold; text-align: center; font-size: 20px;">Publication Ethics and Malpractice Statement(PEMS)</p>
<br/>


<p>Publication ethics are central to the scholarly scientific research publishing to win the trust and the confidence of the research community. Authors, reviewers, editors and publishers are important stakeholders of the publication process and each one of these elements must adhere to the standard ethical practices to enhance the authenticity and the originality of the published work.</p>

<br/>
<p><b>Editor’s Duties and Responsibilities in Upholding Publication Ethics</b></p>
<br/>
<p>This publication/publisher follows the COPE Code of Conduct and the Best Practice Guidelines for the Journal Editors and the Code of Conduct for the Journal Publishers.</p>
<br/>


<p><b>Roles & Responsibilities of the Editor</b></p>
<br/>

<p>•	The Editorial Board comprised of the experts in their respective subjects and it is headed by the Editor-in-Chief. This is the ultimate decision making body in the entire publication process.<br/>
•	The editor in chief/editor is the final authority to decide whether or not the submitted manuscript fits within the scope of the journal. Hence he/she decides to consider or reject the manuscript at the entry/submission level.<br/>
•	The publisher is final in framing the publication policies and the entire editorial board acts in accordance to the policies of the journal. The publisher however should abide by the national and the international publication standards for copyrights and the ethical practices towards animal, human and environment rights backed by the legal cell to resolve conflicts and violations if any in this process.<br/>
•	After having evaluating the manuscript for its relevance, originality and novice, the editor must assign the manuscript for review.<br/>
•	Editors should forward the manuscripts for reviewer’s scrutiny and should abide by the reviewer’s recommendations towards acceptance/rejection of the manuscript for publication.<br/>
•	Editor must act impartial and judicious in assigning the manuscript for review by selecting the right reviewer and keeping the identities of the author/s confidential to avoid bias.<br/>
•	The editor must act transparent while assessing the originality of the manuscript for plagiarism by applying appropriate and universally acceptable software tools to avoid personal bias.<br/>
•	Editor must remain impartial by accepting or rejecting the manuscript based on the reviewer’s comment.<br/>
•	Editor must disclose the conflict of interest if any while performing the editorial duties to remain judicious. Editor must stay away from such decisions in case of any personal, institutional and financial conflict of interests.<br/>
•	Editor must not disclose any unpublished material in the manuscript without the author’s consent.<br/>
•	Editor should stick to the publication style of the publishing house for proofing and formatting of the manuscript and should work with the author in comply with it.<br/>
•	Editor must take steps to see that the manuscripts abide by the editorial policy of the publisher towards disclosure of the information/content.<br/>
</p>


<p><b>Roles & Responsibilities of the Reviewer in Maintaining Publication Standards</b></p>

<br/>

<p>•	Reviewer plays a pivotal role in the entire review process in enhancing the quality of the manuscript based on which the manuscript gets published or rejected.<br/>
•	Reviewer must be a subject expert in the concerned field as s/he should enriches and enhances the value of the manuscript with their critical analysis and suggestions.<br/>
•	Reviewer thus must be truthful in acting as a referee in judging the quality of the manuscript.<br/>
•	The reviewer must remain abreast of the developments taking place in their area of expertise to add value to the manuscript.<br/>
•	Qualifications and the expertise of the reviewer thus are crucial in ensuring the best review standards. Reviewers must therefore disclose their abilities/inability in judging the manuscript.<br/>
•	Publishing industry generally works on deadlines and reviewer must remain punctual in honoring it with their timely review submissions to avoid delay in the publication process.<br/>
•	The reviewer must maintain confidentiality by keeping the decisions intact and should disclose the outcome only to the respective decision making body.<br/>
•	Reviewer must remain objective while making decisions by thoroughly adhering to the research standards. They should examine the copy critically for aspects like duplication, violation of copyrights, and abuse of any clinical ethical practices, including human, animal and the environmental rights.<br/>
•	The reviewer should add value to the final outcome of the manuscript after the review process through knowledge sharing, and enhancement.<br/>
•	Reviewer must closely scrutinize the aspects like referencing style, citations, and the disclosure of information for upholding the national security, and the territorial integrity of the region.<br/>
•	Financial and personal conflicts of interest of the reviewer influence the final outcome of the manuscript. The reviewer must abstain from the review process on such cases.<br/>
•	Reviewers should not review the manuscripts in which they have competitive, collaborative, institutional, and personal conflicts of interest with any of the author/s, and publisher.<br/>
</p>


<p><b>Roles and Responsibilities of the Author in Upholding Publication Ethics :</b></p>

<br/>

<p>•	Author/s remains in the center of the entire publication process as the author takes the initiative to explore an innovative research area to be considered as manuscript for publication. <br/>
•	Author‘s zeal in searching for novelty and their commitment to adhere to originality promotes the best research and publication standards.<br/>
•	Author must be committed to stick to the ethical practices while conducting research and documenting the same as a manuscript.<br/>
•	They must stick to originality and should thoroughly acknowledge the source of information in writing the manuscript.<br/>
•	Any contempt or violation of copyrights is a criminal offence and hence the authors must refrain from such acts by taking prior permission of the copyright holder before publishing.<br/>
•	The author/s must declare that the manuscript submitted is unpublished, original and is not under review with other publisher. Author remains responsible for violations if any and all the disputes can be addressed as per the international publication laws and norms.<br/>
•	Any manuscript that is partially or totally overlap with the already published material can be considered as duplication. Authors must not submit the same manuscript for more than one journal. Such practices are against the International copyright laws, ethical conducts, and cost effective use of resource to ensure originality of the manuscript and such manuscripts are liable to be rejected.<br/>
•	Author/s must take enough care while including images, pictures and figures and usage of any copyrighted image should be duly acknowledged or should be used with prior permission.<br/>
•	Research without siting various source of information is neither possible nor imaginable. Authors must refrain from such citation practices to increase the number of citations of a given author, as it is citation manipulation.<br/>
•	If the final outcome contain manipulated results with fabricated or falsified experiments and data, such manuscript is considered as falsification or fabrication consents.<br/>
•	The authors listed or mentioned in the manuscript must have made a significant contribution towards the research work submitted and they all should abide by the claims made in the manuscript. It is necessary to acknowledge the individual contributions of all the authors including the students and laboratory technicians.<br/>
•	Authors must disclose financial interests or collaborations if any that may directly or indirectly raise the question of bias in the submitted work. Authors must acknowledge the funding or institutional support in the manuscript.<br/>
•	Author must maintain confidentiality towards patient’s identities that have participated in th clinical studies. Disclosure of the names, pictures and other identities affects the privacy of the participants and it is against the research norms.<br/>
•	Patients taking part in the clinical experiments should be well informed of the consequences and side effects if any and their consent should be taken in writing prior to subjecting them to the clinical experiments.<br/>
•	Authors must furnish the approval of the regulatory bodies when the research involves animals, human and bio specimen. Author/s must include a statement on ethics approval at the commencement of the Methods section by mentioning the name and the address of the ethics committee.<br/>
•	Author must act proactive in responding to any criticism, corrections from any quarter and should take steps to rectify discrepancies if any if the article gets published.<br/>
</p>



<p>Ethical Roles and Responsibilities of the Publisher</p>

<br/>

<p>•	Publisher remains responsible for all the published material in print or digital form and hence should take all the measures that would not attract any legal procedure.<br/>
•	Publisher must be aware of all the international publication norms and ethics irrespective of the publication model and must make a statement to this effect to disclose their publication policy.<br/>
•	Publisher in collaboration with the editor in chief should take necessary measures to correct acts of plagiarism or violation of copyright if detected in the publication process.<br/>
•	Publisher must publish an erratum in case of any technical or typological error in the published manuscript by making a clear statement to this effect.<br/>
•	Publisher should retract any publication that is impinges the code of conduct of the research publication.<br/>
•	The Publisher should not discriminate any author or their contribution based on their age, gender, color, religion, creed, disability, marital status, veteran status, national origin, race, genetic predisposition and sexual orientation.<br/>
•	As per the editorial policy of the periodical the publisher must make arrangements to provide access to the published material to its users.<br/>
•	The publisher must take necessary measures in the circulation, preservation, archiving and distribution of the published research by partnering with organizations/institutions.<br/>
</p>


























