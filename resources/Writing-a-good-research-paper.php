<p style="font-weight: bold; text-align: center; font-size: 20px;">Writing a good research paper</p>
<br/>

<p><b>Writing a good research paper</b></p><br/>
<p>A major goal of this course is the development of effective technical writing skills. To help you become an accomplished writer, you will prepare several research papers based upon the studies completed in lab. Our research papers are not typical "lab reports." In a teaching lab a lab report might be nothing more than answers to a set of questions. Such an assignment hardly represents the kind of writing you might be doing in your eventual career.<br/>
                        Written and oral communications skills are probably the most universal qualities sought by graduate and professional schools as well as by employers. You alone are responsible for developing such skills to a high level.

</p><br/>


<p><b>Resources for learning technical writing :</b></p><br/>

<p>Before you begin your first writing assignment, please consult all of the following resources, in order to gain the most benefit from the experience.
</p>

<br/>

<p>•	General form of a typical research article <br/>
•	Specific guidelines (if any) for the assignment – see the writeups on individual lab studies<br/>
•	Writing portfolio examples (pdf)<br/>
•	
As you polish up your writing skills please make use of the following resources<br/>
•	Instructor feedback on previous assignments<br/>
•	Common errors in student research papers<br/>
•	Selected writing rules (somewhat less serious than the other resources)
For Biosciences majors the general guidelines apply to future course work, as can be seen by examining the guidelines for the advanced experimental sciences research paper (Bioc 311).<br/>
</p>
<br/>
<p><b>General form of a research paper</b></p>

<br/>


<p>An objective of organizing a research paper is to allow people to read your work selectively. When I research a topic, I may be interested in just the methods, a specific result, the interpretation, or perhaps I just want to see a summary of the paper to determine if it is relevant to my study. To this end, many journals require the following sections, submitted in the order listed, each section to start on a new page. There are variations of course. Some journals call for a combined results and discussion, for example, or include materials and methods after the body of the paper. The well known journal Science does away with separate sections altogether, except for the abstract.<br/>

Your papers are to adhere to the form and style required for the Journal of Biological Chemistry, requirements that are shared by many journals in the life sciences.
</p>
<br/>

<p><b>General style</b></p>

<br/>

<p>Specific editorial requirements for submission of a manuscript will always supercede instructions in these general guidelines. To make a paper readable</p>
<br/>

<p>•	Print or type using a 12 point standard font, such as Times, Geneva, Bookman, Helvetica, etc. <br/>
•	Text should be double spaced on 8 1/2" x 11" paper with 1 inch margins, single sided <br/>
•	Number pages consecutively <br/>
•	Start each new section on a new page <br/>
•	Adhere to recommended page limits<br/>
</p>
<br/>

<p><b>Mistakes to avoid :</b></p>

<br/>


<p>•	Placing a heading at the bottom of a page with the following text on the next page (insert a page break!)<br/>
•	Dividing a table or figure - confine each figure/table to a single page<br/>
•	Submitting a paper with pages out of order <br/>
</p>


<p>In all sections of your paper :</p>

<p>•	Use normal prose including articles ("a", "the," etc.)<br/>
•	Stay focused on the research topic of the paper<br/>
•	Use paragraphs to separate each important point (except for the abstract)<br/>
•	Indent the first line of each paragraph<br/>
•	Present your points in logical order<br/>
•	Use present tense to report well accepted facts - for example, 'the grass is green'<br/>
•	Use past tense to describe specific results - for example, 'When weed killer was applied, the grass was brown'<br/>
•	Avoid informal wording, don't address the reader directly, and don't use jargon, slang terms, or superlatives<br/>
•	Avoid use of superfluous pictures - include only those figures necessary to presenting results<br/>
</p>
<br/>

<p><b>Title Page :</b></p>
<br/>
<p>Select an informative title as illustrated in the examples in your writing portfolio example package. Include the name(s) and address(es) of all authors, and date submitted. "Biology lab #1" would not be an informative title, for example.</p>
<br/>

<p><b>Abstract :</b></p>
<br/>
<p>The summary should be two hundred words or less. See the examples in the writing portfolio package.</p>
<br/>
<p><b>General intent :</b></p>
<br/>
<p>An abstract is a concise single paragraph summary of completed work or work in progress. In a minute or less a reader can learn the rationale behind the study, general approach to the problem, pertinent results, and important conclusions or new questions. </p>
<br/>

<p><b>Writing an abstract :</b></p>
<p>Write your summary after the rest of the paper is completed. After all, how can you summarize something that is not yet written? Economy of words is important throughout any paper, but especially in an abstract. However, use complete sentences and do not sacrifice readability for brevity. You can keep it concise by wording sentences so that they serve more than one purpose. For example, "In order to learn the role of protein synthesis in early development of the sea urchin, newly fertilized embryos were pulse-labeled with tritiated leucine, to provide a time course of changes in synthetic rate, as measured by total counts per minute (cpm)." This sentence provides the overall question, methods, and type of analysis, all in one sentence. The writer can now go directly to summarizing the results.</p>

<br/>
<p>Summarize the study, including the following elements in any abstract. Try to keep the first two items to no more than one sentence each.</p>
<br/>
<p>•	Purpose of the study - hypothesis, overall question, objective<br/>
•	Model organism or system and brief description of the experiment<br/>
•	Results, including specific data - if the results are quantitative in nature, report quantitative data; results of any statistical analysis shoud be reported<br/>
•	Important conclusions or questions that follow from the experiment(s)<br/>
</p>


<br/>

<p><b>Style:</b></p>
<br/>
<p>•	Single paragraph, and concise<br/>
•	As a summary of work done, it is always written in past tense<br/>
•	An abstract should stand on its own, and not refer to any other part of the paper such as a figure or table<br/>
•	Focus on summarizing results - limit background information to a sentence or two, if absolutely necessary<br/>
•	What you report in an abstract must be consistent with what you reported in the paper<br/>
•	Corrrect spelling, clarity of sentences and phrases, and proper reporting of quantities (proper units, significant figures) are just as important in an abstract as they are anywhere else<br/>
</p>

<br/>

<p><b>Introduction</b></p>
<br/>
<p>Your introductions should not exceed two pages (double spaced, typed). See the examples in the writing portfolio package.</p>

<br/>
<p><b>General intent</b></p>
<br/>
<p>The purpose of an introduction is to aquaint the reader with the rationale behind the work, with the intention of defending it. It places your work in a theoretical context, and enables the reader to understand and appreciate your objectives.</p>

<br/>
<p><b>Writing an introduction</b>
</p>



<br/>
<p>The abstract is the only text in a research paper to be written without using paragraphs in order to separate major points. Approaches vary widely, however for our studies the following approach can produce an effective introduction.</p>

<br/>
<p>•	Describe the importance (significance) of the study - why was this worth doing in the first place? Provide a broad context.<br/>
•	Defend the model - why did you use this particular organism or system? What are its advantages? You might comment on its suitability from a theoretical point of view as well as indicate practical reasons for using it.<br/>
•	Provide a rationale. State your specific hypothesis(es) or objective(s), and describe the reasoning that led you to select them.<br/>
•	Very briefy describe the experimental design and how it accomplished the stated objectives.<br/>
</p>

<br/>



<p><b>Style:</b></p>
<br/>
<p>•	Use past tense except when referring to established facts. After all, the paper will be submitted after all of the work is completed.<br/>
•	Organize your ideas, making one major point with each paragraph. If you make the four points listed above, you will need a minimum of four paragraphs.<br/>
•	Present background information only as needed in order support a position. The reader does not want to read everything you know about a subject.<br/>
•	State the hypothesis/objective precisely - do not oversimplify.<br/>
•	As always, pay attention to spelling, clarity and appropriateness of sentences and phrases.<br/>
</p>

<br/>
<p><b>Materials and Methods</b></p>
<p>There is no specific page limit, but a key concept is to keep this section as concise as you possibly can. People will want to read this material selectively. The reader may only be interested in one formula or part of a procedure. Materials and methods may be reported under separate subheadings within this section or can be incorporated together.</p>

<br/>

<p><b>General intent</b></p>
<p>This should be the easiest section to write, but many students misunderstand the purpose. The objective is to document all specialized materials and general procedures, so that another individual may use some or all of the methods in another study or judge the scientific merit of your work. It is not to be a step by step description of everything you did, nor is a methods section a set of instructions. In particular, it is not supposed to tell a story. By the way, your notebook should contain all of the information that you need for this section.</p>
<br/>

<p><b>Writing a materials and methods section :</b></p>
<br/>
<p><b>Materials:</b></p>

<br/>
<p>•	Describe materials separately only if the study is so complicated that it saves space this way.<br/>
•	Include specialized chemicals, biological materials, and any equipment or supplies that are not commonly found in laboratories.<br/>
•	Do not include commonly found supplies such as test tubes, pipet tips, beakers, etc., or standard lab equipment such as centrifuges, spectrophotometers, pipettors, etc.<br/>
•	If use of a specific type of equipment, a specific enzyme, or a culture from a particular supplier is critical to the success of the experiment, then it and the source should be singled out, otherwise no.<br/>
•	Materials may be reported in a separate paragraph or else they may be identified along with your procedures.<br/>
</p>

<br/>



<p><b>Methods:</b></p>
<br/>

<p>•	See the examples in the writing portfolio package<br/>
•	Report the methodology (not details of each procedure that employed the same methodology)<br/>
•	Describe the mehodology completely, including such specifics as temperatures, incubation times, etc.<br/>
•	To be concise, present methods under headings devoted to specific procedures or groups of procedures<br/>
•	Generalize - report how procedures were done, not how they were specifically performed on a particular day. For example, report "samples were diluted to a final concentration of 2 mg/ml protein;" don't report that "135 microliters of sample one was diluted with 330 microliters of buffer to make the protein concentration 2 mg/ml." Always think about what would be relevant to an investigator at another institution, working on his/her own project.<br/>
</p>


